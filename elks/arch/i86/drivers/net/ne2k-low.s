;------------------------------------------------------------------------------
; NE2K driver - low part - test routines
;------------------------------------------------------------------------------

	IMPORT _main

	.TEXT

;------------------------------------------------------------------------------
; Idle routine
;------------------------------------------------------------------------------

; Halt the processor until next interrupt

_idle:

	hlt
	ret


;------------------------------------------------------------------------------
; Main entry point
;------------------------------------------------------------------------------

; Assume CS = DS = ES = SS (compact binary)

_entry:

	call    _main
	retf


;------------------------------------------------------------------------------

	EXPORT _idle

	ENTRY _entry

	END

;------------------------------------------------------------------------------
